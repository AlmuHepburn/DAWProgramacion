public class Ejercicio3 {

    public static void main(String[] args){

        int operando= 100;
        System.out.println("Valor inicial: "+operando);
        operando+=50;
        System.out.println("Después de sumar 50 (+=): "+operando);
        operando-=30;
        System.out.println("Después de restar 30 (-=): "+operando);
        operando*=2;
        System.out.println("Después de multiplicar por 2 (*=): "+operando);
        operando/=4;
        System.out.println("Después de dividir entre 4 (/=): "+operando);


    }
}
